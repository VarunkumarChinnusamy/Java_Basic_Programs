public class math_function {
    public static void main(String[] args) {
        // math function.......... must start with "Math"
        System.out.println(Math.max(3,6)); // max value
        System.out.println(Math.abs(-3)); // same value nagetive into positive
        System.out.println(Math.min(1,78)); // min value
        System.out.println(Math.ceil(23));  // to decimal value
    }
}
